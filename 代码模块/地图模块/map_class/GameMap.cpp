void GameMap::LoadMap()
{
	LAYERINFO mapinfo;//定义地图信息结构体
	//微结构体中的元素赋值
	mapinfo.first_gid = 1;
	mapinfo.map_cols = MAP_COLS;
	mapinfo.map_rows = MAP_ROWS;
	mapinfo.tile_width = TILE_WIDTH;
	mapinfo.tile_height = FILE_HEIGHT;
	//mapinfo.tile_path = L".\\map\\desert.png";
	mapinfo.tile_path = L".\\res\\台阶完整版2.png";
	vector<vector<int>> barrier_data;
	vector<vector<int>> back_data;
	vector<int> row_data(MAP_COLS);
	for (int r = 0; r < MAP_ROWS; r++)
	{
		barrier_data.push_back(row_data);
		back_data.push_back(row_data);
	}
	/*

	for (int r = 0; r < MAP_ROWS; r++)
	{
		for (int c = 0; c < MAP_COLS; c++)
		{
			//back_data[r][c] = MAP_BKG;

		}

	}*/


	for (int i = 0; i < MAP_ROWS*MAP_COLS; i++)
	{
		int col = i % MAP_COLS;
		int row = i / MAP_COLS;
		back_data[row][col] = BACK[i];

	}

	mapinfo.type_id = LAYER_MAP_BACK;//设置图层类型
	mapinfo.data = back_data;//将以上vector容器转存到地图信息结构
	back = new T_Map(mapinfo);//用完整地图信息构造T_MAP对象
	back->SetPosition(0, 0);
	back->setUpdate(true);//设置更新状态

	//将barrier数组中的图块数据转存到vector容器中
	for (int i = 0; i < MAP_ROWS*MAP_COLS; i++)
	{
		int col = i % MAP_COLS;
		int row = i / MAP_COLS;
		barrier_data[row][col] = BARRIER[i];

	}


	mapinfo.type_id = LAYER_MAP_BARR;//设置图层类型
	mapinfo.data = barrier_data;//将以上vector容器转存到地图信息结构
	barrier = new T_Map(mapinfo);//用完整地图信息构造T_MAP对象
	barrier->SetPosition(0, 0);
	barrier->setUpdate(true);//设置更新状态


}

